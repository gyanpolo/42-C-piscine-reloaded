int	ft_iterative_factorial(int nb)
{
	int	c;

	c = 1;
	if ((nb == 1) || (nb == 0))
		return (1);
	if ((nb < 0) || (nb > 12))
		return (0);
	while (nb > 0)
		c *= nb--;
	return (c);
}
